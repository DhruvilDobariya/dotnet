using System;

namespace OperatorsExpressions
{
    class ArithmethicDemo
    {
        static void Main(string[] args)
        {
            int a = 10;
            int b = 20;

            Console.WriteLine(a + b);
            Console.WriteLine(a - b);
            Console.WriteLine(a * b);
            Console.WriteLine(b / a);
            Console.WriteLine(b % a);
            Console.WriteLine(a++);
            Console.WriteLine(a--);
        }
    }
}
